# Definir una cadena
saludo = "¡Hola, mundo!"

# Imprimir la cadena
print(saludo)

# Obtener la longitud de la cadena
longitud = len(saludo)
print("La longitud de la cadena es:", longitud)

# Convertir la cadena a mayúsculas
saludo_mayusculas = saludo.upper()
print("En mayúsculas:", saludo_mayusculas)

# Reemplazar parte de la cadena
nuevo_saludo = saludo.replace("mundo", "Python")
print("Nuevo saludo:", nuevo_saludo)

# Dividir la cadena en palabras
palabras = saludo.split(" ")
print("Palabras separadas:", palabras)